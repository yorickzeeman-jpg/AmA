export default async function handler(req, res) {
  if (req.method !== 'POST') return res.status(405).json({ error: 'Method not allowed' })

  const { stats, healthScore, escalationAlerts, insights, date, generatedBy } = req.body
  const resendKey = process.env.RESEND_API_KEY
  if (!resendKey) return res.status(500).json({ error: 'RESEND_API_KEY not configured' })

  const healthColor  = healthScore >= 80 ? '#059669' : healthScore >= 60 ? '#d97706' : '#dc2626'
  const healthLabel  = healthScore >= 80 ? 'Healthy' : healthScore >= 60 ? 'Amber' : 'Critical'

  const html = `<!DOCTYPE html>
<html>
<head><meta charset="utf-8"></head>
<body style="margin:0;padding:0;background:#f3f4f6;font-family:Arial,sans-serif;">
<div style="max-width:600px;margin:0 auto;padding:24px 16px;">

  <div style="background:#07122a;border-radius:12px;padding:24px;margin-bottom:16px;">
    <div style="color:#e8680a;font-size:20px;font-weight:900;margin-bottom:4px;">AEB Portal — Daily Operations Report</div>
    <div style="color:rgba(255,255,255,0.5);font-size:12px;">${date} · Generated by ${generatedBy}</div>
    <div style="display:inline-block;margin-top:12px;padding:6px 14px;background:${healthColor}25;border:1px solid ${healthColor}50;border-radius:20px;color:${healthColor};font-size:13px;font-weight:700;">
      Operations Health: ${healthLabel} (${healthScore}/100)
    </div>
  </div>

  <div style="background:#fff;border-radius:12px;padding:20px;margin-bottom:16px;">
    <div style="font-size:14px;font-weight:700;color:#111827;margin-bottom:14px;">Case Summary</div>
    <table style="width:100%;border-collapse:collapse;">
      ${[
        ['Open Cases',       stats.totalOpen,      '#1e5fd9'],
        ['Closed Today',     stats.totalClosed,    '#059669'],
        ['New Today',        stats.newToday,       '#7c3aed'],
        ['SLA Breached',     stats.slaBreached,    '#dc2626'],
        ['No Activity',      stats.noActivity,     '#d97706'],
        ['Pending Billing',  stats.pendingBilling, '#d97706'],
        ['Escalated',        stats.escalated,      '#dc2626'],
      ].map(([l,v,c])=>`
        <tr style="border-bottom:1px solid #f3f4f6;">
          <td style="padding:9px 12px;font-size:13px;color:#6b7280;">${l}</td>
          <td style="padding:9px 12px;font-size:14px;font-weight:700;color:${c};text-align:right;">${v}</td>
        </tr>`).join('')}
    </table>
  </div>

  ${escalationAlerts && escalationAlerts.length > 0 ? `
  <div style="background:#fff;border-radius:12px;padding:20px;margin-bottom:16px;border-left:4px solid #dc2626;">
    <div style="font-size:14px;font-weight:700;color:#dc2626;margin-bottom:12px;">Escalation Alerts (${escalationAlerts.length})</div>
    ${escalationAlerts.slice(0,5).map(({c,esc})=>`
      <div style="padding:8px 0;border-bottom:1px solid #f9fafb;">
        <div style="font-size:12px;font-weight:700;color:#111827;">${c.ref} — ${c.caseTypeName}</div>
        <div style="font-size:11px;color:#6b7280;">Escalate to: ${esc.label}</div>
      </div>`).join('')}
  </div>` : ''}

  ${insights ? `
  <div style="background:#fff;border-radius:12px;padding:20px;margin-bottom:16px;">
    <div style="font-size:14px;font-weight:700;color:#111827;margin-bottom:10px;">AI Insights</div>
    <div style="font-size:13px;color:#374151;line-height:1.7;">${insights.replace(/\n\n/g,'<br><br>')}</div>
  </div>` : ''}

  <div style="text-align:center;font-size:11px;color:#9ca3af;padding:8px;">
    AEB Portal · Amadwala Employee Benefits · ${date}
  </div>
</div>
</body>
</html>`

  try {
    const response = await fetch('https://api.resend.com/emails', {
      method: 'POST',
      headers: { 'Content-Type':'application/json', 'Authorization':`Bearer ${resendKey}` },
      body: JSON.stringify({
        from:    'AEB Portal <onboarding@resend.dev>',
        to:      ['leandre@amadwala.co.za'],
        subject: `AEB Daily Report — ${date} — ${healthLabel} (${healthScore}/100)`,
        html,
      })
    })
    const data = await response.json()
    if (!response.ok) return res.status(502).json({ error: data.message })
    return res.status(200).json({ ok: true })
  } catch(e) {
    return res.status(500).json({ error: e.message })
  }
}
